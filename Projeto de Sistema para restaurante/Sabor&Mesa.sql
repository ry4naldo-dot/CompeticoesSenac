create table empregados(
  id int not null primary key auto_increment,
  nome varchar(255) not null,
  cpf varchar(11) not null,
  inicio_servico time not null,
  final_servico time not null
);

create table clientes(
  id int not null primary key auto_increment,
  nome varchar(255) not null,
  data_nascimento timestamp default current_timestamp,
  cpf varchar(11) not null,
  telefone int not null
);

create table pedidos(
  id int not null primary key auto_increment,
  mesa int not null,
  prato varchar(100) not null,
  valor decimal(8,2) not null,
  cliente_atendido int not null,
  id_empregado int not null,
  foreign key (cliente_atendido) references clientes(id),
  foreign key (id_empregado) references empregados(id)
);

create table agendamentos(
  id int not null primary key auto_increment,
  mesa int not null,
  quantidade_pessoas int not null,
  dia_agendado date not null,
  dia_agendamento timestamp default current_timestamp,
  id_empregado int not null,
  id_cliente int not null,
  foreign key (id_empregado) references empregados(id),
  foreign key (id_cliente) references clientes(id)
);

